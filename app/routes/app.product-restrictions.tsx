import type { HeadersFunction, LoaderFunctionArgs } from "react-router";
import { boundary } from "@shopify/shopify-app-react-router/server";
import { authenticate } from "../shopify.server";

export const loader = async ({ request }: LoaderFunctionArgs) => {
  await authenticate.admin(request);

  return null;
};

export default function ProductRestrictionsPage() {
  return (
    <s-page heading="Product restrictions">
      <s-section heading="Planned configuration">
        <s-paragraph>
          This screen will map configured product tag rules to pincode groups
          and configurable checkout validation messages.
        </s-paragraph>
      </s-section>
    </s-page>
  );
}

export const headers: HeadersFunction = (headersArgs) => {
  return boundary.headers(headersArgs);
};
